package cn.tr1ck.matrixclock.ui.display

/**
 * Icons aligned to the three font heights:
 * - Large: `LARGE_FONT_ROWS` (16)
 * - Medium: `MEDIUM_GLYPH_ROWS` (7)
 * - Mini: `MINI_GLYPH_ROWS` (5)
 *
 * Data is stored as per-row bitmasks (hex). Bit 0 is the right-most pixel in the row.
 */
object MatrixIcons {

    internal fun isOn(rows: IntArray, row: Int, col: Int, width: Int): Boolean {
        if (row !in rows.indices) return false
        if (col !in 0 until width) return false
        val mask = 1 shl (width - 1 - col)
        return (rows[row] and mask) != 0
    }

    internal fun isMiniOn(rows: IntArray, row: Int, col: Int): Boolean =
        isOn(rows = rows, row = row, col = col, width = MINI_GLYPH_ROWS)

    internal fun isLargeOn(rows: IntArray, row: Int, col: Int): Boolean =
        isOn(rows = rows, row = row, col = col, width = LARGE_FONT_ROWS)

    // ---------- Mini icons (5x5) ----------
